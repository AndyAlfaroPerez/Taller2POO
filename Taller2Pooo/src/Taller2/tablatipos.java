    // Busca los indices de ambos tipos y retorna el multiplicador correspondiente
    public static double getEfectividad(String atacante, String defensor) {
        int fila = -1;
        int columna = -1;

        for (int i = 0; i < TIPOS.length; i++) {
            if (TIPOS[i].equalsIgnoreCase(atacante)) {
                fila = i;
            }
            if (TIPOS[i].equalsIgnoreCase(defensor)) {
                columna = i;
            }
        }
        // Si algún tipo no se encuentra, no hay modificador
        if (fila == -1 || columna == -1) {
            return 1.0;
        }

        return EFECTIVIDAD[fila][columna];
    }
